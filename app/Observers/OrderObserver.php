<?php

namespace App\Observers;

use App\Models\Order;

class OrderObserver
{
    /**
     * Handle the Order "created" event.
     */
    public function created(Order $order): void
    {
        $orderCode = $this->generateCode($order);
        $order->update([
            'order_no' => $orderCode
        ]);
    }

    private function generateCode(Order $model)
    {
        $id = $model->id;
        return 'O-' . str_pad($id, 6, '0', STR_PAD_LEFT);
    }

